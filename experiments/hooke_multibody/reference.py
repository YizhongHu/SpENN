"""Independent baseline references for Hooke multibody experiments."""

from __future__ import annotations

from dataclasses import dataclass
from math import exp, pi, sqrt


@dataclass(frozen=True)
class GaussianHartreeReference:
    """Closed-form Gaussian Hartree baseline for a Hooke trap.

    Parameters
    ----------
    n_electrons : int
        Number of electrons in the trap.
    n_up, n_down : int
        Fixed spin-sector counts. The baseline energy does not use spin, but
        the counts are recorded for comparison tables.
    harmonic_omega : float
        Harmonic trap frequency.
    spatial_dim : int
        Spatial dimension supported by the analytic Coulomb formula.
    alpha : float
        Gaussian log-amplitude coefficient in ``log|psi| = -alpha * R^2``.
    kinetic_energy, harmonic_energy, coulomb_energy, total_energy : float
        Analytic expectation values for the uncorrelated Gaussian baseline.
    """

    n_electrons: int
    n_up: int
    n_down: int
    harmonic_omega: float
    spatial_dim: int
    alpha: float
    kinetic_energy: float
    harmonic_energy: float
    coulomb_energy: float
    total_energy: float

    def as_row(self) -> dict[str, object]:
        """Return the baseline as a CSV-ready row.

        Returns
        -------
        dict of str to object
            Reference observable row with scalar energy components.
        """

        return {
            "baseline_available": True,
            "baseline_method": "gaussian_hartree_variational",
            "baseline_high_accuracy": False,
            "n_electrons": self.n_electrons,
            "n_up": self.n_up,
            "n_down": self.n_down,
            "spatial_dim": self.spatial_dim,
            "harmonic_omega": self.harmonic_omega,
            "gaussian_alpha": self.alpha,
            "baseline_kinetic_energy": self.kinetic_energy,
            "baseline_harmonic_energy": self.harmonic_energy,
            "baseline_coulomb_energy": self.coulomb_energy,
            "baseline_energy": self.total_energy,
        }


def gaussian_hartree_reference(
    *,
    n_electrons: int,
    n_up: int,
    n_down: int,
    harmonic_omega: float,
    spatial_dim: int = 3,
    alpha: float | None = None,
) -> GaussianHartreeReference:
    """Return a variational Gaussian Hartree baseline.

    Parameters
    ----------
    n_electrons : int
        Number of electrons.
    n_up, n_down : int
        Spin-sector counts. They must sum to ``n_electrons``.
    harmonic_omega : float
        Harmonic trap frequency.
    spatial_dim : int, optional
        Spatial dimension. The current Coulomb formula supports only ``3``.
    alpha : float or None, optional
        Gaussian coefficient. If ``None``, the coefficient minimizing the
        Gaussian Hartree energy is selected.

    Returns
    -------
    GaussianHartreeReference
        Analytic baseline energy and components.
    """

    _check_system_counts(n_electrons, n_up, n_down)
    selected_alpha = (
        optimal_gaussian_hartree_alpha(
            n_electrons=n_electrons,
            harmonic_omega=harmonic_omega,
            spatial_dim=spatial_dim,
        )
        if alpha is None
        else float(alpha)
    )
    components = gaussian_hartree_energy_components(
        n_electrons=n_electrons,
        harmonic_omega=harmonic_omega,
        alpha=selected_alpha,
        spatial_dim=spatial_dim,
    )
    return GaussianHartreeReference(
        n_electrons=n_electrons,
        n_up=n_up,
        n_down=n_down,
        harmonic_omega=float(harmonic_omega),
        spatial_dim=int(spatial_dim),
        alpha=selected_alpha,
        kinetic_energy=components["kinetic_energy"],
        harmonic_energy=components["harmonic_energy"],
        coulomb_energy=components["coulomb_energy"],
        total_energy=components["total_energy"],
    )


def gaussian_hartree_energy_components(
    *,
    n_electrons: int,
    harmonic_omega: float,
    alpha: float,
    spatial_dim: int = 3,
) -> dict[str, float]:
    """Return analytic Gaussian Hartree energy components.

    The trial density is generated by ``psi(R) = exp(-alpha * sum_i |r_i|^2)``.
    For three spatial dimensions, independent electron coordinates give
    ``E[1 / |r_i - r_j|] = 2 * sqrt(alpha / pi)``.

    Parameters
    ----------
    n_electrons : int
        Number of electrons.
    harmonic_omega : float
        Harmonic trap frequency.
    alpha : float
        Positive Gaussian coefficient.
    spatial_dim : int, optional
        Spatial dimension. Only ``3`` is supported because the Coulomb
        expectation formula is three-dimensional.

    Returns
    -------
    dict of str to float
        ``kinetic_energy``, ``harmonic_energy``, ``coulomb_energy``, and
        ``total_energy``.
    """

    _check_energy_inputs(n_electrons=n_electrons, harmonic_omega=harmonic_omega, alpha=alpha, spatial_dim=spatial_dim)
    pair_count = n_electrons * (n_electrons - 1) / 2.0
    kinetic = 0.5 * n_electrons * spatial_dim * alpha
    harmonic = n_electrons * spatial_dim * harmonic_omega**2 / (8.0 * alpha)
    coulomb = pair_count * 2.0 * sqrt(alpha / pi)
    return {
        "kinetic_energy": kinetic,
        "harmonic_energy": harmonic,
        "coulomb_energy": coulomb,
        "total_energy": kinetic + harmonic + coulomb,
    }


def optimal_gaussian_hartree_alpha(
    *,
    n_electrons: int,
    harmonic_omega: float,
    spatial_dim: int = 3,
    tolerance: float = 1.0e-14,
    max_iterations: int = 200,
) -> float:
    """Minimize the Gaussian Hartree energy over ``alpha``.

    Parameters
    ----------
    n_electrons : int
        Number of electrons.
    harmonic_omega : float
        Positive harmonic trap frequency.
    spatial_dim : int, optional
        Spatial dimension. Only ``3`` is supported.
    tolerance : float, optional
        Bisection stopping tolerance for the derivative root.
    max_iterations : int, optional
        Maximum bisection iterations.

    Returns
    -------
    float
        Positive variationally optimal Gaussian coefficient.
    """

    _check_energy_inputs(n_electrons=n_electrons, harmonic_omega=harmonic_omega, alpha=1.0, spatial_dim=spatial_dim)
    if harmonic_omega <= 0.0:
        raise ValueError("harmonic_omega must be positive to optimize alpha")
    low = 1.0e-12
    high = max(float(harmonic_omega), 1.0)
    while _gaussian_hartree_energy_derivative(
        n_electrons=n_electrons,
        harmonic_omega=harmonic_omega,
        alpha=high,
        spatial_dim=spatial_dim,
    ) < 0.0:
        high *= 2.0
    for _ in range(max_iterations):
        mid = 0.5 * (low + high)
        derivative = _gaussian_hartree_energy_derivative(
            n_electrons=n_electrons,
            harmonic_omega=harmonic_omega,
            alpha=mid,
            spatial_dim=spatial_dim,
        )
        if abs(derivative) < tolerance or (high - low) < tolerance * max(1.0, mid):
            return mid
        if derivative < 0.0:
            low = mid
        else:
            high = mid
    return 0.5 * (low + high)


def gaussian_radial_density_rows(*, alpha: float, bins: int = 64, r_max: float = 6.0) -> list[dict[str, float]]:
    """Return one-body radial probability-density rows.

    Parameters
    ----------
    alpha : float
        Gaussian coefficient in ``log|psi| = -alpha * R^2``.
    bins : int, optional
        Number of radial bins.
    r_max : float, optional
        Maximum radius.

    Returns
    -------
    list of dict
        Rows with ``bin_left``, ``bin_right``, ``radius``, and
        ``probability_density``.
    """

    _check_density_inputs(alpha=alpha, bins=bins, r_max=r_max)
    coefficient = 4.0 * pi * (2.0 * alpha / pi) ** 1.5
    return _density_rows(alpha=2.0 * alpha, coefficient=coefficient, bins=bins, r_max=r_max)


def gaussian_pair_distance_density_rows(*, alpha: float, bins: int = 64, r_max: float = 6.0) -> list[dict[str, float]]:
    """Return pair-distance probability-density rows.

    Parameters
    ----------
    alpha : float
        Gaussian coefficient in ``log|psi| = -alpha * R^2``.
    bins : int, optional
        Number of radial bins.
    r_max : float, optional
        Maximum pair distance.

    Returns
    -------
    list of dict
        Rows with ``bin_left``, ``bin_right``, ``radius``, and
        ``probability_density``.
    """

    _check_density_inputs(alpha=alpha, bins=bins, r_max=r_max)
    coefficient = 4.0 * pi * (alpha / pi) ** 1.5
    return _density_rows(alpha=alpha, coefficient=coefficient, bins=bins, r_max=r_max)


def _density_rows(*, alpha: float, coefficient: float, bins: int, r_max: float) -> list[dict[str, float]]:
    width = r_max / bins
    rows = []
    for index in range(bins):
        left = index * width
        right = (index + 1) * width
        radius = 0.5 * (left + right)
        density = coefficient * radius**2 * exp(-alpha * radius**2)
        rows.append(
            {
                "bin_left": left,
                "bin_right": right,
                "radius": radius,
                "probability_density": density,
            }
        )
    return rows


def _gaussian_hartree_energy_derivative(
    *,
    n_electrons: int,
    harmonic_omega: float,
    alpha: float,
    spatial_dim: int,
) -> float:
    pair_count = n_electrons * (n_electrons - 1) / 2.0
    kinetic_derivative = 0.5 * n_electrons * spatial_dim
    harmonic_derivative = -n_electrons * spatial_dim * harmonic_omega**2 / (8.0 * alpha**2)
    coulomb_derivative = 0.0 if pair_count == 0 else pair_count / sqrt(pi * alpha)
    return kinetic_derivative + harmonic_derivative + coulomb_derivative


def _check_system_counts(n_electrons: int, n_up: int, n_down: int) -> None:
    if n_electrons <= 0:
        raise ValueError("n_electrons must be positive")
    if n_up < 0 or n_down < 0:
        raise ValueError("n_up and n_down must be nonnegative")
    if n_up + n_down != n_electrons:
        raise ValueError("n_up + n_down must equal n_electrons")


def _check_energy_inputs(*, n_electrons: int, harmonic_omega: float, alpha: float, spatial_dim: int) -> None:
    if n_electrons <= 0:
        raise ValueError("n_electrons must be positive")
    if spatial_dim != 3:
        raise ValueError("Gaussian Hartree Coulomb formula currently supports spatial_dim=3 only")
    if harmonic_omega < 0.0:
        raise ValueError("harmonic_omega must be nonnegative")
    if alpha <= 0.0:
        raise ValueError("alpha must be positive")


def _check_density_inputs(*, alpha: float, bins: int, r_max: float) -> None:
    if alpha <= 0.0:
        raise ValueError("alpha must be positive")
    if bins <= 0:
        raise ValueError("bins must be positive")
    if r_max <= 0.0:
        raise ValueError("r_max must be positive")
